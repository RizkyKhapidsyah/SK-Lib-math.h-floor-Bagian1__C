#include <stdio.h>      /* printf */
#include <math.h>       /* floor */
#include <conio.h>

int main() {
	printf("floor of 2.3 is %.1lf\n", floor(2.3));
	printf("floor of 3.8 is %.1lf\n", floor(3.8));
	printf("floor of -2.3 is %.1lf\n", floor(-2.3));
	printf("floor of -3.8 is %.1lf\n", floor(-3.8));
	_getch();
	return 0;
}